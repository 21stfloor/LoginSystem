import mongoose from 'mongoose';

const { Schema, model } = mongoose;

const USER_SCHEMA = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    birthday: {
        type: Date,
        required: true
    },
    gender: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    }
});

export default model('User', USER_SCHEMA, 'Users');
